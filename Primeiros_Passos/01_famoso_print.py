# Primeiro contato com Python: o clássico Hello World
# Diagnosticando se está tudo correto com o Python do pessoal
print("Hello World")

# Adicionando partes interativas: input
curso = input("Digite seu curso: ")
print(curso)

# Concatenando informações na função print: mais versatilidade
nome = input("Digite seu nome: ")
print(nome, curso)

# Deixando o print mais ilustrativo: concatenando com strings e f-strings
print("Meu nome é", nome, "e meu curso é", curso)
print(f"Meu nome é {nome} e meu curso é {curso}")

